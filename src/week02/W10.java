package week02;

public class W10 {
    public static void main(String[] args) {
        System.out.println(3 << 2); // 0011(3) -> 2만큼 이동 1100(12)
        System.out.println(3 << 1); // 0011(3) -> 1만큼 이동 0110(6);

        System.out.println(7 << 1); // 0111(7) -> 1만큼 이동 1110(14);
        System.out.println(5 << 2); // 0101(5) -> 2만큼 이동 0001 0100(20)
    }
}
